def validate_final_content(deck: Dict[str, Any], ctx: BuildContext) -> Dict[str, Any]:
    presentation = deck.get("presentation", {})
    slides = deck.get("slides", [])
    checks: List[Dict[str, Any]] = []

    def add_check(name: str, status: str, notes: str = "") -> None:
        checks.append({"check": name, "status": status, "notes": notes})
        if status == "error":
            ctx.error(f"{name}: {notes}")
        elif status == "warning":
            ctx.warn(f"{name}: {notes}")

    add_check("Presentation object present", "pass" if presentation else "warning", "Missing or empty presentation metadata" if not presentation else "")
    add_check("Slides array present", "pass" if slides else "error", "" if slides else "No slides found")

    title = clean_text(presentation.get("title") or "")
    add_check("Presentation title present", "pass" if title else "warning", "" if title else "Missing presentation.title")

    slide_numbers = []
    for idx, slide in enumerate(slides, 1):
        sn = slide.get("slide_number", idx)
        slide_numbers.append(sn)
        title = clean_text(slide.get("title") or "")
        if not title:
            add_check(f"Slide {idx} title", "warning", "Missing title")
        purpose = clean_text(slide.get("purpose") or "")
        if not purpose:
            add_check(f"Slide {idx} purpose", "warning", "Missing purpose")
        content = slide.get("content") or {}
        bullets = [clean_text(b) for b in as_list(content.get("bullets")) if clean_text(b)]
        if len(bullets) > 5:
            add_check(f"Slide {idx} bullet count", "warning", f"{len(bullets)} bullets; recommended max is 3-5")
        long_bullets = [b for b in bullets if len(words(b)) > 14]
        if long_bullets:
            add_check(f"Slide {idx} bullet length", "warning", f"{len(long_bullets)} bullets exceed ~14 words")
        visual_spec = slide.get("visual_spec") or {}
        if not visual_spec:
            add_check(f"Slide {idx} visual spec", "warning", "Missing visual_spec")
        evidence = collect_slide_evidence_refs(slide)
        if not evidence:
            add_check(f"Slide {idx} evidence", "warning", "No evidence references found")

    duplicates = [num for num, count in Counter(slide_numbers).items() if count > 1]
    add_check("Slide numbers unique", "pass" if not duplicates else "warning", f"Duplicate slide numbers: {duplicates}" if duplicates else "")
    add_check("Slide count consistency", "pass", f"Detected {len(slides)} slides")

    return {"checks": checks, "slide_count": len(slides), "errors": ctx.errors, "warnings": ctx.warnings}


def collect_slide_evidence_refs(slide: Dict[str, Any]) -> List[str]:
    refs: List[str] = []
    for ev in as_list(slide.get("evidence_sources")):
        if isinstance(ev, dict):
            ref = ev.get("evidence_id") or ev.get("source") or ev.get("source_file")
            if ref:
                refs.append(clean_text(ref))
        else:
            refs.append(clean_text(ev))
    content = slide.get("content") or {}
    for stat in as_list(content.get("key_stats")):
        if isinstance(stat, dict) and stat.get("source"):
            refs.append(clean_text(stat.get("source")))
    if slide.get("evidence_to_use"):
        refs.extend(clean_text(x) for x in as_list(slide.get("evidence_to_use")))
    return [r for r in refs if r]


