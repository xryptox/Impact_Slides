def resolve_brand_theme(brand: Dict[str, Any], ctx: BuildContext) -> Dict[str, Any]:
    colors = extract_brand_colors(brand)
    fonts = extract_brand_fonts(brand)
    fallbacks_used: List[str] = []

    if colors:
        non_neutral = [c for c in colors if not is_neutral_color(c)]
        accent = non_neutral[0] if non_neutral else colors[0]
        # Choose a light/paper background by default for readability unless deck appears dark-heavy.
        dark_candidates = [c for c in colors if luminance(c) < 0.18]
        light_candidates = [c for c in colors if luminance(c) > 0.82]
        slide_bg = light_candidates[0] if light_candidates else "#F8FAFC"
        stage_bg = dark_candidates[0] if dark_candidates else darken(accent, 0.72)
        text = choose_text_color(slide_bg)
        muted = "#475569" if text == "#111827" else "#CBD5E1"
        panel = "#FFFFFF" if luminance(slide_bg) > 0.72 else lighten(slide_bg, 0.12)
    else:
        fallbacks_used.append("No valid brand colors found; using neutral executive palette")
        accent = "#2563EB"
        slide_bg = "#F8FAFC"
        stage_bg = "#0F172A"
        text = "#111827"
        muted = "#475569"
        panel = "#FFFFFF"

    if contrast_ratio(text, slide_bg) < 4.5:
        text = choose_text_color(slide_bg)
        fallbacks_used.append("Adjusted text color for contrast")
    if contrast_ratio(accent, slide_bg) < 2.2:
        accent = darken(accent, 0.35) if luminance(slide_bg) > 0.6 else lighten(accent, 0.55)
        fallbacks_used.append("Adjusted accent color for visibility")

    display_font = fonts[0] if fonts else "Manrope"
    body_font = fonts[1] if len(fonts) > 1 else display_font
    if not fonts:
        fallbacks_used.append("No brand fonts found; using Manrope")

    theme = {
        "available": bool(brand),
        "colors": {
            "stage_bg": stage_bg,
            "slide_bg": slide_bg,
            "panel_bg": panel,
            "text_primary": text,
            "text_secondary": muted,
            "accent": accent,
            "accent_light": lighten(accent, 0.82),
            "accent_dark": darken(accent, 0.35),
            "border": "#D9E2EC" if text == "#111827" else "rgba(255,255,255,0.18)",
            "brand_palette": colors[:12],
        },
        "fonts": {
            "display": display_font,
            "body": body_font,
            "fallback_stack": "Arial, sans-serif",
            "web_font_link": build_google_font_link(display_font, body_font),
        },
        "layout": {
            "stage_width": STAGE_W,
            "stage_height": STAGE_H,
            "slide_padding": 96,
            "content_gap": 42,
            "corner_radius": 28,
        },
        "fallbacks_used": fallbacks_used,
        "contrast": {
            "text_on_slide_bg": contrast_ratio(text, slide_bg),
            "accent_on_slide_bg": contrast_ratio(accent, slide_bg),
        },
    }
    ctx.theme = theme
    for item in fallbacks_used:
        ctx.warn(item)
    return theme


def extract_brand_colors(brand: Dict[str, Any]) -> List[str]:
    colors: List[str] = []

    def walk(obj: Any) -> None:
        if isinstance(obj, dict):
            for key, value in obj.items():
                if str(key).lower() in {"color", "hex"}:
                    c = parse_hex_color(value)
                    if c:
                        colors.append(c)
                walk(value)
        elif isinstance(obj, list):
            for item in obj:
                walk(item)
        elif isinstance(obj, str):
            for match in re.findall(r"#[0-9a-fA-F]{6}\b", obj):
                colors.append(match.upper())

    walk(brand)
    result = []
    seen = set()
    for c in colors:
        c = c.upper()
        if c not in seen:
            seen.add(c)
            result.append(c)
    # Avoid returning only transparent theme:* pseudo-values; parse_hex_color filters those out.
    return result


def extract_brand_fonts(brand: Dict[str, Any]) -> List[str]:
    font_candidates: List[str] = []

    def add_font(value: Any) -> None:
        text = clean_text(value)
        if not text:
            return
        # Drop obviously non-font text.
        if len(text) > 80 or text.lower().startswith("observed"):
            return
        if text.lower() in {"none", "null", "unknown"}:
            return
        font_candidates.append(text)

    def walk(obj: Any, parent_key: str = "") -> None:
        if isinstance(obj, dict):
            for key, value in obj.items():
                key_l = str(key).lower()
                if key_l in {"font", "font_name", "name"} and "font" in parent_key.lower():
                    add_font(value)
                elif key_l == "font" or "font" in key_l or "typography" in key_l:
                    if isinstance(value, str):
                        # Extract names from comma-separated prose lightly.
                        for part in re.split(r"[,;\n]", value):
                            add_font(part.strip(" .'\"`"))
                    else:
                        walk(value, key_l)
                else:
                    walk(value, key_l)
        elif isinstance(obj, list):
            for item in obj:
                walk(item, parent_key)

    walk(brand)
    cleaned: List[str] = []
    seen = set()
    banned = {"theme", "body", "display", "heading", "headings", "fonts", "typography", "top_fonts"}
    for f in font_candidates:
        f = re.sub(r"\s+", " ", f).strip()
        f = f.split("|")[0].strip()
        if not f or f.lower() in banned or f.startswith("#"):
            continue
        if f.lower() not in seen:
            seen.add(f.lower())
            cleaned.append(f)
    return cleaned[:4]


