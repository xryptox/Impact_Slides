def pptx_add_textbox(
    slide: Any,
    text: str,
    x: float,
    y: float,
    w: float,
    h: float,
    size: float,
    color: str,
    font: str,
    bold: bool = False,
    align: Optional[Any] = None,
) -> Any:
    shape = slide.shapes.add_textbox(Inches(x), Inches(y), Inches(w), Inches(h))
    tf = shape.text_frame
    tf.clear()
    tf.word_wrap = True
    try:
        tf.auto_size = MSO_AUTO_SIZE.TEXT_TO_FIT_SHAPE
    except Exception:
        pass
    p = tf.paragraphs[0]
    p.text = clean_text(text)
    if align is not None:
        p.alignment = align
    p.font.name = font
    p.font.size = Pt(size)
    p.font.bold = bold
    p.font.color.rgb = rgbcolor(color)
    return shape


def pptx_add_bullets(slide: Any, bullets: List[str], x: float, y: float, w: float, h: float, theme: Dict[str, Any], size: float = 19) -> Any:
    colors = theme["colors"]
    font = theme["fonts"]["body"]
    shape = slide.shapes.add_textbox(Inches(x), Inches(y), Inches(w), Inches(h))
    tf = shape.text_frame
    tf.clear()
    tf.word_wrap = True
    for idx, bullet in enumerate(bullets[:6]):
        p = tf.paragraphs[0] if idx == 0 else tf.add_paragraph()
        p.text = clean_text(bullet)
        p.level = 0
        p.font.name = font
        p.font.size = Pt(size)
        p.font.color.rgb = rgbcolor(colors["text_primary"])
        p.space_after = Pt(12)
    return shape


