def extract_visual_steps(slide: Dict[str, Any]) -> List[str]:
    visual = slide.get("visual_spec") or {}
    primary = visual.get("primary_visual") or {}
    raw = primary.get("steps_or_data") or primary.get("steps") or primary.get("data") or []
    result: List[str] = []
    for item in as_list(raw):
        if isinstance(item, dict):
            text = item.get("label") or item.get("title") or item.get("name") or item.get("step") or item.get("value") or json.dumps(item, ensure_ascii=False)
        else:
            text = item
        text = clean_text(text)
        if text:
            result.append(text)
    return result


def collect_slide_asset_refs(slide: Dict[str, Any]) -> List[str]:
    refs: List[str] = []
    refs.extend(clean_text(x) for x in as_list(slide.get("visual_assets_references")))
    visual = slide.get("visual_spec") or {}
    refs.extend(clean_text(x) for x in as_list(visual.get("assets_to_use")))
    for sv in as_list(visual.get("supporting_visuals")):
        if isinstance(sv, dict):
            refs.append(clean_text(sv.get("value_or_content") or sv.get("asset") or ""))
    return [r for r in refs if r]


