def now_iso() -> str:
    return dt.datetime.now().replace(microsecond=0).isoformat()


def default_output_dir(input_json: Path) -> Path:
    stamp = dt.datetime.now().strftime("%Y%m%d_%H%M%S")
    return input_json.parent / f"ImpactSlide_Step4_Output_{stamp}"


def clean_text(value: Any) -> str:
    if value is None:
        return ""
    text = str(value).replace("\x00", " ")
    text = re.sub(r"[ \t]+", " ", text)
    text = re.sub(r"\n{3,}", "\n\n", text)
    return text.strip()


def slugify(value: str, fallback: str = "item") -> str:
    slug = re.sub(r"[^a-zA-Z0-9]+", "-", clean_text(value).lower()).strip("-")
    return slug or fallback


def words(text: str) -> List[str]:
    return re.findall(r"\b[\w'-]+\b", clean_text(text))


def load_json(path: Path) -> Any:
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except json.JSONDecodeError:
        # Optional recovery if json_repair is available.
        try:
            from json_repair import repair_json  # type: ignore

            return json.loads(repair_json(path.read_text(encoding="utf-8")))
        except Exception:
            raise


def write_json(path: Path, data: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(data, indent=2, ensure_ascii=False, default=json_default), encoding="utf-8")


def write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def json_default(obj: Any) -> Any:
    if isinstance(obj, (dt.datetime, dt.date)):
        return obj.isoformat()
    if isinstance(obj, Path):
        return str(obj)
    return str(obj)


def as_list(value: Any) -> List[Any]:
    if value is None:
        return []
    if isinstance(value, list):
        return value
    if isinstance(value, tuple):
        return list(value)
    return [value]


def html_escape(value: Any) -> str:
    return html.escape(clean_text(value), quote=True)


def shorten(value: Any, max_chars: int = 160) -> str:
    text = clean_text(value)
    if len(text) <= max_chars:
        return text
    return text[: max_chars - 1].rstrip() + "…"


def parse_hex_color(value: Any) -> Optional[str]:
    if value is None:
        return None
    text = clean_text(value).upper()
    m = re.search(r"#[0-9A-F]{6}\b", text)
    if m:
        return m.group(0)
    # Accept bare RGB hex from python-pptx color extraction.
    m = re.fullmatch(r"[0-9A-F]{6}", text)
    if m:
        return "#" + text
    return None


def hex_to_rgb_tuple(value: str) -> Tuple[int, int, int]:
    value = parse_hex_color(value) or "#000000"
    return int(value[1:3], 16), int(value[3:5], 16), int(value[5:7], 16)


def luminance(hex_color: str) -> float:
    r, g, b = [x / 255 for x in hex_to_rgb_tuple(hex_color)]

    def channel(c: float) -> float:
        return c / 12.92 if c <= 0.03928 else ((c + 0.055) / 1.055) ** 2.4

    return 0.2126 * channel(r) + 0.7152 * channel(g) + 0.0722 * channel(b)


def contrast_ratio(color_a: str, color_b: str) -> float:
    l1 = luminance(color_a)
    l2 = luminance(color_b)
    lighter, darker = max(l1, l2), min(l1, l2)
    return round((lighter + 0.05) / (darker + 0.05), 2)


def is_neutral_color(hex_color: str) -> bool:
    r, g, b = hex_to_rgb_tuple(hex_color)
    max_delta = max(abs(r - g), abs(g - b), abs(r - b))
    avg = (r + g + b) / 3
    return max_delta < 18 or avg < 24 or avg > 235


def choose_text_color(bg: str) -> str:
    return "#FFFFFF" if luminance(bg) < 0.42 else "#111827"


def lighten(hex_color: str, factor: float = 0.88) -> str:
    r, g, b = hex_to_rgb_tuple(hex_color)
    r = int(r + (255 - r) * factor)
    g = int(g + (255 - g) * factor)
    b = int(b + (255 - b) * factor)
    return f"#{r:02X}{g:02X}{b:02X}"


def darken(hex_color: str, factor: float = 0.2) -> str:
    r, g, b = hex_to_rgb_tuple(hex_color)
    r = int(r * (1 - factor))
    g = int(g * (1 - factor))
    b = int(b * (1 - factor))
    return f"#{r:02X}{g:02X}{b:02X}"


def rgbcolor(hex_color: str) -> Any:
    r, g, b = hex_to_rgb_tuple(hex_color)
    if RGBColor is None:
        return None
    return RGBColor(r, g, b)


def file_to_data_uri(path: Path, max_image_dim: int = 1800, quality: int = 86) -> Optional[str]:
    if not path.exists() or not path.is_file():
        return None
    mime = mimetypes.guess_type(str(path))[0] or "application/octet-stream"
    data = path.read_bytes()
    # Resize very large raster images before embedding to keep HTML manageable.
    if Image is not None and mime.startswith("image/") and path.suffix.lower() not in {".svg", ".gif"}:
        try:
            with Image.open(path) as img:
                if max(img.size) > max_image_dim:
                    img = img.convert("RGB") if img.mode not in {"RGB", "RGBA"} else img.copy()
                    img.thumbnail((max_image_dim, max_image_dim), Image.LANCZOS)
                    import io

                    buf = io.BytesIO()
                    fmt = "PNG" if img.mode == "RGBA" else "JPEG"
                    mime = "image/png" if fmt == "PNG" else "image/jpeg"
                    img.save(buf, format=fmt, quality=quality, optimize=True)
                    data = buf.getvalue()
        except Exception:
            pass
    return f"data:{mime};base64," + base64.b64encode(data).decode("ascii")


