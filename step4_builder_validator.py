def normalize_final_json(data: Dict[str, Any]) -> Dict[str, Any]:
    """Accept both Final Slide JSON and small variations from GPT output."""
    presentation = data.get("presentation") or data.get("presentation_plan") or {}
    slides = data.get("slides") or data.get("final_slides") or data.get("slide_update_plan") or []
    if not isinstance(slides, list):
        slides = []
    normalized_slides = []
    for idx, slide in enumerate(slides, 1):
        if not isinstance(slide, dict):
            continue
        content = slide.get("content") or {}
        if not isinstance(content, dict):
            content = {"body_text": clean_text(content)}
        visual_spec = slide.get("visual_spec") or {}
        if not isinstance(visual_spec, dict):
            visual_spec = {"overall_layout_description": clean_text(visual_spec)}
        normalized = dict(slide)
        normalized.setdefault("slide_number", idx)
        normalized.setdefault("section", slide.get("section") or "What")
        normalized.setdefault("title", slide.get("proposed_title") or slide.get("title") or f"Slide {idx}")
        normalized.setdefault("purpose", slide.get("new_slide_purpose") or slide.get("purpose") or "")
        normalized.setdefault("audience_takeaway", slide.get("audience_takeaway") or slide.get("key_message") or "")
        normalized.setdefault("layout_type", slide.get("layout_type") or infer_layout_type(slide))
        normalized["content"] = content
        normalized["visual_spec"] = visual_spec
        normalized_slides.append(normalized)
    return {"presentation": presentation, "slides": normalized_slides}


def infer_layout_type(slide: Dict[str, Any]) -> str:
    visual_spec = slide.get("visual_spec") or {}
    primary_visual = visual_spec.get("primary_visual") if isinstance(visual_spec, dict) else {}
    visual_type = clean_text((primary_visual or {}).get("type", "")).lower()
    title = clean_text(slide.get("title") or slide.get("proposed_title") or "").lower()
    content = slide.get("content") or {}
    if slide.get("slide_number") == 1 or "title" in title:
        return "title_or_opening"
    if "process" in visual_type or "flow" in visual_type:
        return "full_process_flow"
    if "timeline" in visual_type or "roadmap" in visual_type:
        return "timeline"
    if "dashboard" in visual_type or content.get("key_stats"):
        return "metric_dashboard"
    if "table" in visual_type or "comparison" in visual_type or "grid" in visual_type:
        return "comparison_grid"
    if "quote" in visual_type:
        return "quote_card"
    return "split_text_visual"


