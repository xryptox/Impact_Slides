def slide_to_html(slide: Dict[str, Any], idx: int, total: int, theme: Dict[str, Any], asset_map: Dict[str, str], density: str) -> str:
    layout = clean_text(slide.get("layout_type") or infer_layout_type(slide))
    title = slide.get("title") or f"Slide {idx}"
    section = slide.get("section") or ""
    content = slide.get("content") or {}
    headline = content.get("headline") or slide.get("audience_takeaway") or slide.get("purpose") or ""
    bullets = [clean_text(b) for b in as_list(content.get("bullets")) if clean_text(b)]
    evidence = collect_slide_evidence_refs(slide)
    slide_classes = f"slide layout-{slugify(layout)}"
    active = " active visible" if idx == 1 else ""
    slide_number = f"{idx:02d} / {total:02d}"

    if idx == 1 or layout == "title_or_opening":
        return render_html_title_slide(slide, idx, total, active)
    if layout == "metric_dashboard":
        body = render_html_metric_slide(slide, idx, total)
    elif layout in {"full_process_flow", "timeline", "roadmap"}:
        body = render_html_process_slide(slide, idx, total)
    elif layout in {"comparison_grid", "data_table"}:
        body = render_html_comparison_slide(slide, idx, total)
    elif layout == "quote_card":
        body = render_html_quote_slide(slide, idx, total)
    else:
        body = render_html_default_slide(slide, idx, total, asset_map)

    evidence_html = f'<div class="evidence-row">Evidence: {html_escape(", ".join(evidence[:8]))}</div>' if evidence else ""
    return f"""            <section class=\"{slide_classes}{active}\" data-slide-number=\"{idx}\">
                <div class=\"slide-number\">{html_escape(slide_number)}</div>
{body}
{evidence_html}
            </section>"""


