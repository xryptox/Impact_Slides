def parse_args(argv: Optional[Sequence[str]] = None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Build/validate PPTX and standalone HTML decks from Final_Slide_Content.json.")
    parser.add_argument("--input", "-i", required=True, help="Path to Final_Slide_Content.json")
    parser.add_argument("--brand", "-b", default=None, help="Path to brand_style_summary.json")
    parser.add_argument("--output", "-o", default=None, help="Output folder")
    parser.add_argument("--assets", default=None, help="Path to asset_manifest.json")
    parser.add_argument("--asset-root", default=None, help="Folder containing source image/assets to embed in HTML")
    parser.add_argument("--pptx", action="store_true", help="Generate updated_presentation.pptx")
    parser.add_argument("--html", action="store_true", help="Generate standalone presentation.html")
    parser.add_argument("--export-html-pdf", action="store_true", help="Export HTML deck to presentation.pdf using Playwright")
    parser.add_argument("--density", choices=["speaker-led", "reading-first"], default="speaker-led", help="HTML content density mode")
    parser.add_argument("--open-html", action="store_true", help="Open generated HTML in default browser")
    parser.add_argument("--validate-visual", action="store_true", help="Run Playwright visual validation on generated HTML")
    parser.add_argument("--title", default=None, help="Override presentation title")
    return parser.parse_args(argv)


def main(argv: Optional[Sequence[str]] = None) -> int:
    args = parse_args(argv)
    ctx = BuildContext()

    input_path = Path(args.input).expanduser().resolve()
    if not input_path.exists():
        print(f"ERROR: input JSON not found: {input_path}", file=sys.stderr)
        return 2
    output_dir = Path(args.output).expanduser().resolve() if args.output else default_output_dir(input_path).resolve()
    output_dir.mkdir(parents=True, exist_ok=True)

    # If neither output flag is set, generate both by default.
    generate_pptx = args.pptx or (not args.pptx and not args.html)
    generate_html = args.html or (not args.pptx and not args.html)

    try:
        raw = load_json(input_path)
        if not isinstance(raw, dict):
            raise ValueError("Input JSON must be an object")
        deck = normalize_final_json(raw)
        if args.title:
            deck.setdefault("presentation", {})["title"] = args.title
    except Exception as exc:  # noqa: BLE001
        print(f"ERROR: failed to load input JSON: {exc}", file=sys.stderr)
        return 2

    brand: Dict[str, Any] = {}
    if args.brand:
        brand_path = Path(args.brand).expanduser().resolve()
        if brand_path.exists():
            try:
                brand = load_json(brand_path)
                if not isinstance(brand, dict):
                    ctx.warn("brand_style_summary.json did not contain a JSON object; using fallback theme")
                    brand = {}
            except Exception as exc:  # noqa: BLE001
                ctx.warn(f"Could not load brand file {brand_path}: {exc}")
        else:
            ctx.warn(f"Brand file not found: {brand_path}; using fallback theme")
    else:
        ctx.warn("No brand file supplied; using fallback theme")

    validation = validate_final_content(deck, ctx)
    theme = resolve_brand_theme(brand, ctx)

    asset_manifest = None
    if args.assets:
        asset_path = Path(args.assets).expanduser().resolve()
        if asset_path.exists():
            try:
                asset_manifest = load_json(asset_path)
            except Exception as exc:  # noqa: BLE001
                ctx.warn(f"Could not load asset manifest {asset_path}: {exc}")
        else:
            ctx.warn(f"Asset manifest not found: {asset_path}")
    asset_root = Path(args.asset_root).expanduser().resolve() if args.asset_root else None
    asset_map = build_asset_map(asset_manifest, asset_root, ctx) if generate_html else {}

    pptx_path = output_dir / "updated_presentation.pptx"
    html_path = output_dir / "presentation.html"
    pdf_path = output_dir / "presentation.pdf"
    visual_validation = None

    if generate_pptx:
        try:
            build_pptx(deck, theme, pptx_path, ctx)
        except Exception as exc:  # noqa: BLE001
            ctx.error(f"PPTX generation failed: {exc}")
            ctx.outputs["pptx_error_traceback"] = traceback.format_exc(limit=5)

    if generate_html:
        try:
            build_html(deck, theme, asset_map, html_path, args.density, ctx)
            html_validation = validate_html_file(html_path, len(deck.get("slides", [])), theme, ctx)
            if args.validate_visual:
                visual_validation = visual_validate_with_playwright(html_path, output_dir, ctx)
            if args.export_html_pdf:
                export_html_to_pdf(html_path, pdf_path, ctx)
            if args.open_html:
                webbrowser.open(html_path.as_uri())
        except Exception as exc:  # noqa: BLE001
            ctx.error(f"HTML generation failed: {exc}")
            ctx.outputs["html_error_traceback"] = traceback.format_exc(limit=5)

    report = {
        "generated_at": now_iso(),
        "input": str(input_path),
        "output_dir": str(output_dir),
        "validation": validation,
        "theme": theme,
        "outputs": ctx.outputs,
        "errors": ctx.errors,
        "warnings": ctx.warnings,
        "infos": ctx.infos,
        "html_validation": ctx.html_validation,
        "visual_validation": visual_validation,
        "import_warnings": IMPORT_WARNINGS,
    }
    write_json(output_dir / "validation_report.json", report)
    write_text(output_dir / "validation_report.md", render_validation_report_md(report))

    print(f"Done. Outputs written to: {output_dir}")
    if ctx.errors:
        print(f"Completed with {len(ctx.errors)} error(s). See validation_report.md", file=sys.stderr)
        # Do not fail hard if optional PPTX/PDF dependency is missing; validation report captures it.
        return 1 if not ctx.outputs else 0
    if ctx.warnings:
        print(f"Completed with {len(ctx.warnings)} warning(s). See validation_report.md")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
