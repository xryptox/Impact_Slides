def validate_html_file(html_path: Path, expected_slide_count: int, theme: Dict[str, Any], ctx: BuildContext) -> Dict[str, Any]:
    text = html_path.read_text(encoding="utf-8")
    checks = []

    def add(name: str, status: str, notes: str = "") -> None:
        checks.append({"check": name, "status": status, "notes": notes})
        if status == "error":
            ctx.error(f"HTML: {name}: {notes}")
        elif status == "warning":
            ctx.warn(f"HTML: {name}: {notes}")

    if BeautifulSoup is not None:
        soup = BeautifulSoup(text, "html.parser")
        slides = soup.select(".slide")
        add(".deck-viewport exists", "pass" if soup.select_one(".deck-viewport") else "error")
        add(".deck-stage exists", "pass" if soup.select_one(".deck-stage") else "error")
        add(".slide count", "pass" if len(slides) == expected_slide_count else "warning", f"Expected {expected_slide_count}, found {len(slides)}")
        add("First slide active", "pass" if slides and "active" in slides[0].get("class", []) else "warning")
    else:
        add("beautifulsoup4 available", "warning", "Install beautifulsoup4 for deeper HTML validation")
        slide_count = 0
        for class_attr in re.findall(r'class="([^"]*)"', text):
            tokens = re.split(r"\s+", class_attr.strip())
            if "slide" in tokens:
                slide_count += 1
        add(".slide count", "pass" if slide_count == expected_slide_count else "warning", f"Expected {expected_slide_count}, found {slide_count}")

    add("Fixed stage CSS width", "pass" if "width: 1920px" in text else "error")
    add("Fixed stage CSS height", "pass" if "height: 1080px" in text else "error")
    add("Uses active/visible switching", "pass" if ".slide.active" in text and ".slide.visible" in text else "error")
    slide_switching_display_none = bool(re.search(r"\.slide[^{}]*\{[^{}]*display\s*:\s*none", text, flags=re.IGNORECASE | re.DOTALL))
    add("Avoids display:none slide switching", "pass" if not slide_switching_display_none else "warning", "display:none found on .slide rule" if slide_switching_display_none else "")
    add("Reduced motion support", "pass" if "prefers-reduced-motion" in text else "warning")
    add("Embedded asset support", "pass" if "data:image/" in text or "assets_to_use" not in text else "warning")
    add("Brand accent token used", "pass" if theme["colors"]["accent"] in text else "warning")

    result = {"html_path": str(html_path), "checks": checks}
    ctx.html_validation = result
    return result


def visual_validate_with_playwright(html_path: Path, output_dir: Path, ctx: BuildContext) -> Dict[str, Any]:
    try:
        from playwright.sync_api import sync_playwright  # type: ignore
    except Exception as exc:  # noqa: BLE001
        msg = f"Playwright unavailable: {exc}. Install with: python -m pip install playwright && python -m playwright install chromium"
        ctx.warn(msg)
        return {"status": "skipped", "reason": msg}

    console_errors: List[str] = []
    try:
        with sync_playwright() as p:
            browser = p.chromium.launch()
            page = browser.new_page(viewport={"width": STAGE_W, "height": STAGE_H}, device_scale_factor=1)
            page.on("console", lambda msg: console_errors.append(msg.text) if msg.type == "error" else None)
            page.goto(html_path.resolve().as_uri(), wait_until="networkidle")
            metrics = page.evaluate(
                """() => Array.from(document.querySelectorAll('.slide')).map((s, i) => ({
                    slide: i + 1,
                    width: s.offsetWidth,
                    height: s.offsetHeight,
                    scrollWidth: s.scrollWidth,
                    scrollHeight: s.scrollHeight,
                    textLength: s.innerText.length,
                    overflowRisk: s.scrollWidth > s.clientWidth + 2 || s.scrollHeight > s.clientHeight + 2
                }))"""
            )
            screenshot_dir = output_dir / "html_screenshots"
            screenshot_dir.mkdir(parents=True, exist_ok=True)
            if metrics:
                page.screenshot(path=str(screenshot_dir / "slide_001.png"), full_page=False)
            browser.close()
        result = {"status": "ok", "console_errors": console_errors, "slide_metrics": metrics, "screenshot_dir": str(screenshot_dir)}
        if console_errors:
            ctx.warn(f"HTML console errors detected: {len(console_errors)}")
        return result
    except Exception as exc:  # noqa: BLE001
        ctx.warn(f"Playwright visual validation failed: {exc}")
        return {"status": "error", "error": str(exc), "traceback": traceback.format_exc(limit=4)}


def export_html_to_pdf(html_path: Path, pdf_path: Path, ctx: BuildContext) -> Optional[Path]:
    try:
        from playwright.sync_api import sync_playwright  # type: ignore
    except Exception as exc:  # noqa: BLE001
        ctx.error(f"Cannot export PDF: Playwright unavailable: {exc}")
        return None
    try:
        with sync_playwright() as p:
            browser = p.chromium.launch()
            page = browser.new_page(viewport={"width": STAGE_W, "height": STAGE_H}, device_scale_factor=1)
            page.goto(html_path.resolve().as_uri(), wait_until="networkidle")
            page.emulate_media(media="print")
            pdf_path.parent.mkdir(parents=True, exist_ok=True)
            page.pdf(path=str(pdf_path), width="1920px", height="1080px", print_background=True, prefer_css_page_size=True)
            browser.close()
        ctx.outputs["html_pdf"] = str(pdf_path)
        return pdf_path
    except Exception as exc:  # noqa: BLE001
        ctx.error(f"HTML to PDF export failed: {exc}")
        return None


# ----------------------------- reporting -----------------------------


def render_validation_report_md(report: Dict[str, Any]) -> str:
    lines = [
        "# Step 4 Builder / Validator Report",
        "",
        f"Generated: {report.get('generated_at')}",
        "",
        "## Outputs",
        "",
    ]
    outputs = report.get("outputs") or {}
    if outputs:
        for key, value in outputs.items():
            lines.append(f"- **{key}:** `{value}`")
    else:
        lines.append("No output files generated.")

    lines.extend(["", "## Errors", ""])
    errors = report.get("errors") or []
    lines.extend([f"- {e}" for e in errors] if errors else ["None"])

    lines.extend(["", "## Warnings", ""])
    warnings = report.get("warnings") or []
    lines.extend([f"- {w}" for w in warnings] if warnings else ["None"])

    lines.extend(["", "## Theme", ""])
    theme = report.get("theme") or {}
    colors = (theme.get("colors") or {}) if isinstance(theme, dict) else {}
    fonts = (theme.get("fonts") or {}) if isinstance(theme, dict) else {}
    lines.append(f"- **Accent:** `{colors.get('accent')}`")
    lines.append(f"- **Slide background:** `{colors.get('slide_bg')}`")
    lines.append(f"- **Text:** `{colors.get('text_primary')}`")
    lines.append(f"- **Display font:** `{fonts.get('display')}`")
    lines.append(f"- **Body font:** `{fonts.get('body')}`")
    if theme.get("fallbacks_used"):
        lines.append("- **Fallbacks used:** " + "; ".join(theme.get("fallbacks_used")))

    lines.extend(["", "## Validation Checks", ""])
    checks = ((report.get("validation") or {}).get("checks") or [])
    lines.append("| Check | Status | Notes |")
    lines.append("|---|---|---|")
    for check in checks:
        lines.append(f"| {check.get('check')} | {check.get('status')} | {str(check.get('notes') or '').replace('|', '/')} |")

    html_validation = report.get("html_validation") or {}
    if html_validation:
        lines.extend(["", "## HTML Validation", "", "| Check | Status | Notes |", "|---|---|---|"])
        for check in html_validation.get("checks", []):
            lines.append(f"| {check.get('check')} | {check.get('status')} | {str(check.get('notes') or '').replace('|', '/')} |")

    visual_validation = report.get("visual_validation")
    if visual_validation:
        lines.extend(["", "## Visual Validation", "", f"- **Status:** {visual_validation.get('status')}"])
        if visual_validation.get("console_errors"):
            lines.append(f"- **Console errors:** {len(visual_validation.get('console_errors'))}")
        if visual_validation.get("screenshot_dir"):
            lines.append(f"- **Screenshot dir:** `{visual_validation.get('screenshot_dir')}`")

    lines.extend(["", "## Package Import Warnings", ""])
    if IMPORT_WARNINGS:
        for pkg, warning in sorted(IMPORT_WARNINGS.items()):
            lines.append(f"- `{pkg}`: {warning}")
    else:
        lines.append("None")
    return "\n".join(lines) + "\n"


